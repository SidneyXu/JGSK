object App {

    def main(args: Array[String]) {
        //  If
        val x = 1
        val y = 2
        if (x + y == 0) {
            println("x+y==3")
        } else {
            println("x+y!=3")
        }

        if (x + y == 3)
            println("x+y==3")
        else
            println("x+y!=3")
    }

}
